    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        if(txtNomeCliente.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Informe o nome do cliente");
        }
        else {
            try {
                cbxAnimal.setEnabled(true);
                List<Cliente> cli = new LinkedList<Cliente>();
                List<Animal> ani = new LinkedList<Animal>();
                Cliente c = new Cliente();
                Animal a = new Animal();
                
                
                cli = cdao.getClientesByName(txtNomeCliente.getText());
                JOptionPane.showMessageDialog(null, adao.getAnimaisDoCliente(c.getId()));   
                
                for(int aux = 0; aux < cli.size(); aux++){
                    
                    c.setId(cli.get(aux).getId());
                
                }
            } catch (SQLException ex) {
                Logger.getLogger(frmAgendar.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

